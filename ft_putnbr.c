#include "ft_printf.h"

int	ft_putnbr(int n)
{
	char	c;
	int		all;

	all = 0;
	if (n == -2147483648)
	{
		write(1, "-2147483648", 11);
		all += 11;
	}
	if (n < 0 && n != -2147483648)
	{
		n *= -1;
		write(1, "-", 1);
		all++;
	}
	if (n <= 9 && n != -2147483648)
	{
		c = n + '0';
		write(1, &c, 1);
		all++;
	}
	if (n > 9)
		all += ft_putnbr(n / 10) + ft_putnbr(n % 10);
	return (all);
}
